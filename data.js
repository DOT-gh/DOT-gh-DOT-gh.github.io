/**
 * Форматирует дату для отображения
 * @param {Date|number|string} date - объект даты, timestamp или строка
 * @param {boolean} includeTime - включить ли время в результат
 * @returns {string} отформатированная дата
 */
function formatDate(date, includeTime = false) {
    const d = new Date(date);
    const options = {
        day: 'numeric',
        month: 'long',
        year: 'numeric'
    };
    
    if (includeTime) {
        options.hour = '2-digit';
        options.minute = '2-digit';
    }
    
    return d.toLocaleDateString('ru-RU', options);
}

/**
 * Возвращает случайный элемент из массива
 * @param {Array} array - исходный массив
 * @returns {*} случайный элемент
 */
function getRandomItem(array) {
    if (!array || !array.length) return null;
    const randomIndex = Math.floor(Math.random() * array.length);
    return array[randomIndex];
}

/**
 * Перемешивает массив (алгоритм Fisher-Yates)
 * @param {Array} array - исходный массив
 * @returns {Array} перемешанный массив
 */
function shuffleArray(array) {
    const result = [...array];
    for (let i = result.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [result[i], result[j]] = [result[j], result[i]];
    }
    return result;
}

/**
 * Разбивает массив на чанки (группы) указанного размера
 * @param {Array} array - исходный массив
 * @param {number} size - размер группы
 * @returns {Array} массив групп
 */
function chunkArray(array, size) {
    const result = [];
    for (let i = 0; i < array.length; i += size) {
        result.push(array.slice(i, i + size));
    }
    return result;
}

